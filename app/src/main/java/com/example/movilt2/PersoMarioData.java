package com.example.movilt2;

public class PersoMarioData {
    private final String image;
    private final String name;


    public PersoMarioData(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

}
